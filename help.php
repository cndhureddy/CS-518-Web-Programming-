<A name=Question14></A><p><font face="arial, helvetica, sans-serif" color="#990000"><strong>Uploading profile Picture</font></strong><p>
<HR width="100%">

<ol>
  <li>Login to your account</li>
  <li>Click on the dropdown button on the top-left corner(where workspace name is displayed)of the home page</li>
  <li>From the dropdown list, select "Profile & account" option and it directs to a page </li>
  <li>Click on the "Choose File" option and browse the profile picture. The picture should have minimum height and width of 250 px and maximum height and width of 750 px</li>
  <li>Click update picture option </li>
</ol> 
 <hr> 

 <A name=Question15></A><br><p><font face="arial, helvetica, sans-serif" color="#990000"><strong> How to Create Channels</font></strong><p>
<HR width="100%">
 
  <ol>
  <li>Login to your account</li>
  <li>Click on the Channels + option on the left side of the page</li>
  <li>Select a unique channel name with 5-100 characters</li>
  <li>Choose public or private option from the dropdown menu of privacy setting accoring to your requirement. </li>
  <li>You can describe purpose of the channel under purpose option. It is not mandatory </li>
 <li>Click Submit</li>
</ol> 
 <hr> 



 <A name=Question16></A><br><p><font face="arial, helvetica, sans-serif" color="#990000"><strong>How to Invite members to channels? </font></strong><p>
<HR width="100%">
 
<ol>
  <li>Enter the channel by selecting the channel from left side bar of the home page</li>
  <li>Click on the invite_members button displayed on the top of the channel page </li>
  <li>You can see list of members that can be invited with "invite member" option beside their user names which should be clicked to invite a member</li>
  
</ol> 
 <hr> 
 
 <A name=Question17></A><br><p><font face="arial, helvetica, sans-serif" color="#990000"><strong>How do I Sign out?</font></strong><p>
<HR width="100%">
 
<ol>
  <li>Click on the dropdown button on the top-left corner(where workspace name is displayed)of the home page</li>
  <li>From the dropdown list, select "Sign Out of slack cs odu." option and it directs to a page </li>
</ol> 
 <hr>  
 
 <A name=Question18></A><br><p><font face="arial, helvetica, sans-serif" color="#990000"><strong>How to react to posts in a channel?</font></strong><p>
<HR width="100%">
 
<ol>
  <li>When you open a particular channel, you see posts from various users.</li>
  <li> Beside the post you can find like, dislike and reply icons. Use these icons to react to the post </li>
   <li>By selecting reply icon, a window pops up where you can enter message in the text box and then click on "send" to send it</li>
</ol> 
 <hr>  
 <A name=Question19></A><br><p><font face="arial, helvetica, sans-serif" color="#990000"><strong>How do I post messages in channels ?</font></strong><p>
<HR width="100%">
<ol>
  <li>When you open a particular channel, you see a text box in the bottom of the page.</li>
  <li>Enter the message in the text box and click on the send icon beside the text box to post the message. </li>
   <li>You can post code snippet, image or url to the channel by selecting the respective icon in the bottom right corner of the page and then by clicking on the send icon. </li>
</ol> 
 <hr>  
 <br>

